     * @param  \App\Models\news  $news
     * @return \Illuminate\Http\Response
     */
     public function update(Request $request, $id)
     {
         $news = news::find($id);
         $news->id = $request->id;
         $news->title = $request->title;
         $news->email = $request->email;
         $news->save();
         return redirect()->action('NewsController@index');
     }

